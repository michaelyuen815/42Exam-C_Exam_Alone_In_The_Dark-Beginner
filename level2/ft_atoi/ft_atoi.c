int	ft_atoi(const char *str)
{
	int sign;
	int	ret;

	if (!str || !*str)
		return (0);
	sign = 1;
	ret = 0;
	while (*str == 32 || *str == 9 || *str == 10)
		str++;
	if (*str == '+' || *str == '-')
		sign = (*(str++) == '-' ? -1 : 1);
	while (*str > 47 && *str < 58)
		ret = ret * 10 + (*(str++) - '0');
	return (ret * sign);
}
